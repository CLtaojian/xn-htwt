public enum ECreditBizType {

    NEW_CAR("1", "新车"), SECOND_CAR("2", "二手车");

    ECreditBizType(String code, String value) {
        this.code = code;
        this.value = value;
    }

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public String getValue() {
        return value;
    }

}
